def _build_records() -> list[dict]:
    """Build records: prefer authoritative JSON, fall back to inline curated set."""
    json_path = PROJECT_ROOT / "data" / "reference" / "gbt4754_2017.json"
    if json_path.is_file():
        return _build_records_from_json(json_path)
    return _build_records_inline()


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--db", required=True)
    p.add_argument("--dry-run", action="store_true")
    args = p.parse_args()

    records = _build_records()
    by_tier: dict[str, int] = {}
    for r in records:
        by_tier[r["_tier"]] = by_tier.get(r["_tier"], 0) + 1

    print(f"[seed] BusinessActivity records prepared: {len(records)}")
    for tier, n in sorted(by_tier.items()):
        print(f"  {tier}: {n} rows")

    if args.dry_run:
        print("[DRY-RUN] no DB writes; exit 0")
        return 0

    import kuzu

    db = kuzu.Database(args.db)
    conn = kuzu.Connection(db)

    t0 = time.time()
    added = 0
    skipped = 0
    failed: list[str] = []
    for r in records:
        try:
            conn.execute("MERGE (n:BusinessActivity {id: $id})", {"id": r["id"]})
            conn.execute(
                "MATCH (n:BusinessActivity {id: $id}) SET n.name=$name, n.description=$descx",
                {"id": r["id"], "name": r["name"], "descx": r["description"]},
            )
            added += 1
        except Exception as e:
            msg = str(e).lower()
            if "primary key" in msg or "duplicate" in msg:
                skipped += 1
            else:
                failed.append(f"{r['id']}: {str(e)[:120]}")

    elapsed = time.time() - t0
    print(
        f"[APPLY] BusinessActivity: added={added} skipped(dup)={skipped} "
        f"failed={len(failed)} elapsed={int(elapsed*1000)}ms"
    )
    if failed:
        for line in failed[:10]:
            print(f"  FAIL {line}")

    record_ingestion(
        source_file=__file__,
        rows_written={"BusinessActivity": added},
        duration_s=elapsed,
        dry_run=False,
        note=f"GB/T 4754 sections+divisions+priority-groups; skipped(dup)={skipped} failed={len(failed)}",
    )
    return 0 if not failed else 1


if __name__ == "__main__":
    sys.exit(main())
